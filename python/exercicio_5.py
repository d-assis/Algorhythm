# -*- coding: utf-8 -*-

# variaveis
valor1 = 10
operador = "/"
valor2 = 10

# Deve ser impresso 1.0
valor1 = int(valor1)	
valor2 = int(valor2)	
if operador == "+":
	r = valor1 + valor2
	print("A soma deve dar: ")
	print(r)	
if operador == "-":
	r = valor1 - valor2
	print("A subtração deve dar: ")
	print(r)	
if operador == "/":
	r = valor1 / valor2
	print("A divisão deve dar: ")
	print(r)	
if operador == "*":
	r = valor1 * valor2
	print("A multiplicação deve dar: ")
	print(r)